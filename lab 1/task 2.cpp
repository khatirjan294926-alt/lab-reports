#include <iostream> // Header file for input and output
using namespace std; 
int main()
{
 int num1, num2, sum; // Declare three integer variables
 
 cout << "Enter first number: ";
 cin >> num1; // Take first number input
 
 cout << "Enter second number: ";
 cin >> num2; // Take second number input
 
 sum = num1 + num2; // Add two numbers
 
 cout << "Sum = " << sum; // Display result
 
 return 0;
}