#include <iostream>
#include <string>
using namespace std;

int main() {

    // Declare and initialize strings
    string firstName = "Aziz";
    string lastName = "Ahmad";

    // Concatenate strings
    string fullName = firstName + " " + lastName;

    // Display result
    cout << "First Name: " << firstName << endl;
    cout << "Last Name: " << lastName << endl;
    cout << "Full Name: " << fullName << endl;

    return 0;
}